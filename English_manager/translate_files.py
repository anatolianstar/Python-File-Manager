def translate_text(text):
    """Translate Turkish text to English"""
    # Sort by length (longest first) to avoid partial replacements
    sorted_translations = sorted(translations.items(), key=lambda x: len(x[0]), reverse=True)
    
    for turkish, english in sorted_translations:
        # Use word boundaries to avoid partial replacements
        pattern = r'\b' + re.escape(turkish) + r'\b'
        text = re.sub(pattern, english, text, flags=re.IGNORECASE)
    
    return text

def translate_file(input_file, output_file):
    """Translate a single file"""
    print(f"Translating {input_file} -> {output_file}")
    
    try:
        with open(input_file, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Translate content
        translated_content = translate_text(content)
        
        # Write translated content
        with open(output_file, 'w', encoding='utf-8') as f:
            f.write(translated_content)
        
        print(f"‚úÖ Successfully translated {input_file}")
        
    except Exception as e:
        print(f"‚ùå Error translating {input_file}: {e}")

